public class AssignmentOperators {
    public static void main(String args[]){
        //Assignment Operators are (=,+=,-=,/=,%=)
        int a=30;
        int b=20;
        a+=b;//here a=50
        System.out.println(a);
        a-=b;//here a=30
        System.out.println(a);
        a*=b;//here a=60
        System.out.println(a);
        a/=b;//here a=30
        System.out.println(a);
        a%=b;//here a=10
        System.out.println(a);
    }
}
